fun main(args: Array<String>){
    /**
     * Casting Practice
     * */

    var a :Int = 5
    var b :Double= 10.25
    var c :Char= 'A'

    //Integer to Double
    println("Int to Double: "+ a.toDouble())

    //Double to Integer
    println("Double to Int: "+b.toInt())

    //Integer to char
    println("Int to Char: " +65.toChar())

    //Char to Int
    println("Char to Int: "+c.toInt())
}